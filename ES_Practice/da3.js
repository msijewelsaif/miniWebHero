// If-Else
let age = 20;
if (age >= 18) {
    console.log("Adult");
} else {
    console.log("Minor");
}

// Switch
let day = 2;
switch (day) {
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    default:
        console.log("Another day");
}

// Loops
for (let i = 0; i < 5; i++) {
    console.log(i);
}

let i = 0;
while (i < 5) {
    console.log(i);
    i++;
}
